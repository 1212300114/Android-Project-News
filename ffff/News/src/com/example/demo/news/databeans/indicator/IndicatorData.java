package com.example.demo.news.databeans.indicator;


public class IndicatorData {
	private int ret;
	private IndicatorDataArray data;
	private String msg;

	public int getRet() {
		return ret;
	}

	public void setRet(int ret) {
		this.ret = ret;
	}

	public IndicatorDataArray getData() {
		return data;
	}

	public void setData(IndicatorDataArray data) {
		this.data = data;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

}
