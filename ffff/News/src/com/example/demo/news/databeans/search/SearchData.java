package com.example.demo.news.databeans.search;

public class SearchData {

	private int ret;
	private String msg;
	private SearchDataDetail data;

	public int getRet() {
		return ret;
	}

	public void setRet(int ret) {
		this.ret = ret;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

	public SearchDataDetail getData() {
		return data;
	}

	public void setData(SearchDataDetail data) {
		this.data = data;
	}

}
