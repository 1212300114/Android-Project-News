package com.example.demo.news.databeans.dynamic;

public class DynamicData {
	private int ret;
	private String msg;
	private DynamicDataDetail data;

	public int getRet() {
		return ret;
	}

	public void setRet(int ret) {
		this.ret = ret;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

	public DynamicDataDetail getData() {
		return data;
	}

	public void setData(DynamicDataDetail data) {
		this.data = data;
	}

}
